// Package branchpred contains the main implementation of the branch prediction simulator.
// It is a simple simulator that takes in a sequence of branch instructions and simulates the
// various branch prediction algorithms. The simulator can be configured to use different
// branch prediction algorithms and different branch predictor configurations.
//
// The package contains the following files:
//
// branch_prediction.go: Contains the main implementation of the branch prediction simulator.
//
// always_taken.go: Contains the implementation of the always taken branch prediction algorithm.
//
// two_bit.go: Contains the implementation of the two bit branch prediction algorithm.
//
// gshare.go: Contains the implementation of the gshare branch prediction algorithm.
//
// profiled.go: Contains the implementation of the profiled branch prediction algorithm.
package branchpred
